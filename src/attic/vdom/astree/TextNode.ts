import { AstNode } from "./AstNode";
import { Visitor } from "./Visitor";

/* ================================================================================================================= */

export class TextNode extends AstNode
{
    constructor (readonly text: string)
    {
        super();
    }
    
    public receive(visitor: Visitor)
    {
        visitor.visitTextNode(this);
    }
}

