export interface Predicate<T>
{
    (item: T): boolean
}

/* ================================================================================================================= */

export function toPredicate<T>(item: T | Predicate<T>): Predicate<T>
{
    return (typeof item === 'function') ? item as Predicate<T> : (x: T) => x == item;
}

